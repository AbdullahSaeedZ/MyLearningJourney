// Singly Circular Linked List:
// - Each node has: [data | next]
// - The "next" of the last node points back to the head
// - There is no NULL pointer in the list
// - You can traverse forever in one direction (next)


// Doubly Circular Linked List:
// - Each node has: [prev | data | next]
// - The "next" of the last node points to the head
// - The "prev" of the head points back to the last node
// - Traversal can go in both directions (forward and backward)
// - Also, no NULL pointers (completely circular)
