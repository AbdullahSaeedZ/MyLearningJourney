// ================== Data Structure ==================
// - A data structure is a way to organize, store, and retrieve data 
//   inside a program (in memory/RAM).
// - The data exists only while the program is running. Once the program 
//   stops, the data is lost (unless explicitly saved).
// - A program is essentially = Data Structures + Algorithms.
// - Examples: Array, Vector, Linked List, Stack, Queue, Heap, Binary Tree, Graph.
// - Even basic data types like int, float, and string can be considered 
//   as simple data structures because they define how data is stored in memory.
// - Data structures are optimized for speed and efficiency when accessing 
//   and manipulating data during program execution.

// ================== Database ==================
// - A database is a permanent storage system for data, stored on disk 
//   (hard drive/SSD).
// - Data remains available even after the program is closed.
// - Databases are usually managed through software called Database Management System (DBMS).
// - Examples of DBMS: MySQL, Oracle, PostgreSQL, Microsoft Access, SQL Server.
// - Data is typically organized in tables (rows and columns).
// - Databases are optimized for persistence, scalability, and complex querying.
// - Unlike in-memory data structures, databases can handle very large datasets 
//   and allow multiple users to access data concurrently.
