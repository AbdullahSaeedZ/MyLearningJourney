/*
    Encapsulation vs Abstraction (Clear Relation)

    Encapsulation:
    - Group related data and behavior inside a single class.
    - Do NOT expose the internal variables directly.
    - Access to the data should only be through the object (properties/methods).
    - This prevents careless or incorrect modification.

    Abstraction:
    - After the internal details are encapsulated,
      we choose what to SHOW to the outside and what to HIDE.
    - The user only interacts with simple public functions,
      without needing to know how things work internally.

    Relationship:
    - Encapsulation is about *organizing and protecting* the internal details.
    - Abstraction is about *presenting only what matters* to the user.

    Example:
    - A TV remote has buttons to control volume and channels.
    - You do NOT need to know how the TV processes signals internally (Abstraction).
    - The complex circuits and logic inside the TV are grouped and hidden in one device (Encapsulation).

    One-line Summary:
    Encapsulation = store and protect the details inside.
    Abstraction   = reveal only what the user needs.
*/




