CRUD
Create -Read-Update-Delete
CRUD operations are the four fundamental database actions used in almost every system.



C = Create
Insert new records into the database
Implemented using the INSERT statement

--------------------------------------

R = Read
Retrieve data from the database
Implemented using the SELECT statement

--------------------------------------

U = Update
Modify existing records in the database
Implemented using the UPDATE statement

--------------------------------------

D = Delete
Remove records from the database
Implemented using the DELETE statement

--------------------------------------

Any real-world software system relies on CRUD operations
because managing data always involves creating, reading,
updating, and deleting records.
