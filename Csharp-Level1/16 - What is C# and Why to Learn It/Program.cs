// What is C#?
// → C# (pronounced "C-Sharp") is a general-purpose, object-oriented programming language, does not have FP like C++
// → First released in 2002, created specially for .Net by Microsoft and we cant use it without .NET and CLR.
// → Commonly used for building desktop, web, mobile, and game applications.
// → It’s C-based, meaning it’s close to C++ and syntactically similar to Java (Java is c-like not c-based).
// → Modern and powerful, designed for quick and robust application development.

// Key Characteristics
// → Simple: Code is easy to read, write, and understand (similar to Java).
// → Type-Safe: Ensures variables only store compatible data types.
// → Object-Oriented: Supports classes, objects, inheritance, and polymorphism.
// → High-Level: Focuses on logic while abstracting away hardware details.
// → Strong Community: Huge support base and fast answers for C# questions online.

// Why is it called "C#"?
// → The name comes from musical notation where the “sharp” (♯) symbol means a note is raised by a semitone.
// → Symbolically, C# is considered an "elevated" or more advanced version of C.

// What can we do with C#?
// → Everything supported by the .NET platform.
// → Build desktop apps, web services, APIs, mobile apps, IoT, and even games (via Unity).



