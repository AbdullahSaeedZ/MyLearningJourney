            // ===============================================
            // 🔹 Summary: Forms & Controls in Desktop Apps
            // ===============================================
            //
            // - A Form is a window or page in desktop applications.
            //
            // - Controls are tools placed inside forms
            //   (e.g., Button, Label, TextBox, CheckBox, RadioButton, etc.)
            //
            // - Each Control has:
            //     → Properties (like size, color, font, enabled/disabled)
            //     → Events (actions like click, hover, change)
            //
            // - Properties define how the control looks or behaves.
            //   Example: button1.Enabled = false;
            //
            // - Events are triggered when user interacts with the control.
            //   Example: button1.Click → runs specific code when pressed.
            //
            // - Controls can affect each other when an event occurs.
            //   Example: clicking a button changes a label text or disables a textbox.
            //
            // ===============================================
            // 🔹 Common Controls
            // ===============================================
            //
            // Form:
            // - The main window or page that holds all controls.
            //
            // Label:
            // - Displays static text or information. Example: "Enter your name:"
            //
            // TextBox:
            // - Allows user to input text. Example: user types their name.
            //
            // Button:
            // - Executes code when clicked. Example: Submit, Save, or Exit buttons.
            //
            // CheckBox:
            // - Lets user select multiple options (true/false). Example: "Remember me"
            //
            // RadioButton:
            // - Lets user pick only one option from a group. Example: Gender selection
            //
            // ComboBox:
            // - Dropdown list with predefined options. Example: Country selector
            //
            // ListBox:
            // - Shows a list of items; user can select one or more.
            //
            // PictureBox:
            // - Displays an image (logo, icon, etc.)
            //
            // ProgressBar:
            // - Visually shows progress of a task. Example: loading or download progress
            //
            // ===============================================



        }
    }
