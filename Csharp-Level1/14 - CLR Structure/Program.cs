// ==================== CLR STRUCTURE =====================


// Class Loader
// → Loads classes, types, and assemblies into memory before execution so the program can use them.

// IL Compilers (JIT)
// → Translates Intermediate Language (IL) code into native CPU instructions at runtime for actual execution.

// Code Manager
// → Controls how code is executed, tracks stack frames, and manages structured exception handling.

// Garbage Collector
// → Automatically frees memory by removing objects that are no longer referenced, preventing memory leaks.

// Security Engine
// → Enforces code access security, verifies permissions, and protects against unauthorized or unsafe code execution.

// Type Checker
// → Validates that data types are used correctly to ensure type safety and prevent invalid operations at runtime.

// Thread Support
// → Manages concurrent execution of multiple threads, allowing applications to perform several tasks simultaneously.

// Exception Manager
// → Detects, throws, and handles runtime errors gracefully without crashing the entire program.

// Debug Engine
// → Provides debugging, profiling, and diagnostic tools to inspect runtime behavior and detect issues.

// COM Marshaler
// → Handles communication and data conversion between .NET managed code and unmanaged COM components.

// Base Class Library
// → A comprehensive library of predefined classes, interfaces, and types that provide core functionality to .NET programs.



