/*******************************************************
 * 🌐 Common Language Specification (CLS)
 *******************************************************/

/*
 * 1️⃣ Definition
 * -----------------------------------------------------
 * - CLS stands for **Common Language Specification**.
 * - It’s a set of rules and standards defined by the CLR
 *   that all .NET languages must follow.
 * - Its goal: ensure **cross-language compatibility**.
 */

/*
 * 2️⃣ Purpose
 * -----------------------------------------------------
 * - Every .NET language has unique syntax and features,
 *   but CLS defines a common subset that all languages share.
 * - Any feature or data type that follows CLS rules can be
 *   used safely across different .NET languages.
 *
 *   Example:
 *     - A C# class that follows CLS can be used in VB.NET.
 *     - Or VB.NET code can inherit from a C# base class.
 */

/*
 * 3️⃣ Relationship to CTS
 * -----------------------------------------------------
 * - CTS defines *what types exist* and how they behave.
 * - CLS defines *which parts* of those types are allowed
 *   to be shared across all languages.
 *
 *   Think of it like this:
 *     CTS = full toolbox 🧰
 *     CLS = the tools everyone can safely use together 🔧
 */

/*
 * 4️⃣ Why It Matters
 * -----------------------------------------------------
 * - Enables language interoperability (توافقيه بين اللغات):
 *     ✔ cross-language inheritance
 *     ✔ shared libraries
 *     ✔ easier debugging and maintenance
 * - Promotes consistent behavior across all .NET languages.
 */




