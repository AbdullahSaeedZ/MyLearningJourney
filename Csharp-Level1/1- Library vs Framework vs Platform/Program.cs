/*******************************************************
 * ⚙️ Difference Between Library, Framework, and Platform
 *******************************************************/

/*
 * 🔹 Library
 *
 * A library is like a "toolbox" that contains ready-made
 * functions and classes to help you perform specific tasks.
 * It doesn’t force you to follow any particular structure —
 * YOU decide when and how to use it.
 *
 * In short: your code calls the library.
 *
 * 🎯 Examples:
 * - STL in C++ (cmath, string, and so on)
 * - NumPy in Python
 */


/*
 * 🔹 Framework
 *
 * A framework provides a structured way to build applications.
 * The key difference from a library is **control flow**:
 * the framework calls your code, not the other way around.
 *
 * Instead of you calling its functions, it decides when to
 * execute your code as part of its lifecycle.
 *
 * 📘 Simply put:
 * - With a library: you use the code.
 * - With a framework: the code uses you.
 *
 * 🎯 Examples:
 * - ASP.NET Core
 * - Django
 * - React.js
 */


/*
 * 🔹 Platform
 *
 * A platform is a complete runtime environment that provides
 * everything needed to develop and run applications.
 * It includes the operating system, tools, libraries, and frameworks.
 *
 * In short: it’s the foundation where everything runs.
 *
 * 🎯 Examples:
 * - Windows
 * - Linux
 * - .NET Platform
 */


/*
 * 💡 Quick Summary
 * -----------------------------------------------------
 * | Concept    | Control Flow        | Includes              | Example                |
 * |-------------|--------------------|------------------------|------------------------|
 * | Library     | Developer          | Functions + Classes    | STL, NumPy             |
 * | Framework   | Framework Itself   | Libraries + Structure  | ASP.NET, Django, React |
 * | Platform    | Full Environment   | OS + Tools + Framework | .NET, Windows, Linux   |
 * -----------------------------------------------------
 */


/*
 * 🛠️ Practical Analogy
 *
 * Imagine you're building a house:
 * - Library: your toolbox (hammer, saw, screwdriver).
 * - Framework: the building plan and rules you must follow.
 * - Platform: the city that provides electricity, water, and roads.
 */


