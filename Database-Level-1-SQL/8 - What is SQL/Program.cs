// SQL = Structured Query Language
// sometimes pronounced as See-Quel.

// SQL is used to communicate with a data base
// it lets us access and manipulate data bases

// SQL is a standard language that will work with different DBMS (but very small syntax difference sometimes) like:
// Oracle, Sybase, Microsoft SQL Server, Access, Ingers, etc.




// Types of SQL statements (SQL Sub-languages)

// 1) Data Definition Language (DDL)
// Definition = structure of data bases.
// Used to define or modify database objects.
// Examples: CREATE, ALTER, DROP, TRUNCATE.

// 2) Data Manipulation Language (DML)
// Manipulation = change the data itself.
// Used to INSERT new data, UPDATE existing data, DELETE data.

// 3) Data Control Language (DCL)
// Control = permissions.
// Used to manage user access.
// Examples: GRANT (give access), REVOKE (remove access).

// 4) Transaction Control Language (TCL)
// Transaction = confirm or undo changes.
// Controls the behavior of transactions.
// Examples: COMMIT (save), ROLLBACK (undo), SAVEPOINT (partial rollback).

// 5) Data Query Language (DQL)
// Query = fetching data.
// Only one main command: SELECT.


