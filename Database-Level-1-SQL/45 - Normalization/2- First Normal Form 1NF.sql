-- First Normal Form 1NF

-- it is the first form to be applied to be able to do other forms

-- first step is to have a PK in the table:
-- A primary key: A unique identifier for each record in the table.

-- second step is to have Atomic Values:
-- Atomic values: Each column should contain only a single value, and each value should be indivisible (cant be two values) or multi-valued attribute.
-- Note: Here, atomicity states that a single cell cannot hold multiple values. It must hold only a single-valued attribute.
-- The First normal form disallows the multi-valued attribute, composite attribute, and their combinations.

-- Third Step is to have Distinct Name for each column, no repeating groups of columns


